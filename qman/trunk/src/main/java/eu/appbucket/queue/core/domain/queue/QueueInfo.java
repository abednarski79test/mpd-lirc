package eu.appbucket.queue.core.domain.queue;

public class QueueInfo {
	
	private int queueId;
	private String name;

	public int getQueueId() {
		return queueId;
	}
	
	public void setQueueId(int queueId) {
		this.queueId = queueId;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}
